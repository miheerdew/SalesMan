def GetIconBundleFromImage(image, sizes=(8,16,32,64)):
    iconBundle = wx.IconBundle()
    for i in sizes:
        if isinstance(i,int):
            w, h = i, i
        else:
            w, h = i
        converted_img = image.Scale(w,h)
        bmp = converted_img.ConvertToBitmap()
        icon = wx.IconFromBitmap(bmp)
        iconBundle.AddIcon(icon)
    return iconBundle
